def init_database():
    print("⏳ 正在连接数据库 seer_data.db ...")
    conn = sqlite3.connect("seer_data.db")
    cursor = conn.cursor()

    cursor.execute('''
        CREATE TABLE IF NOT EXISTS spirits (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name_cn TEXT NOT NULL,
            attribute1 TEXT NOT NULL,
            attribute2 TEXT
        )
    ''')

    cursor.execute('DELETE FROM spirits')
    cursor.execute('DELETE FROM sqlite_sequence WHERE name="spirits"')
    print("🗑️ 旧数据已清理完毕。")

    count = 0
    for name, attr1, attr2 in MASSIVE_ELVES_DATA:
        cursor.execute(
            'INSERT INTO spirits (name_cn, attribute1, attribute2) VALUES (?, ?, ?)',
            (name, attr1, attr2)
        )
        count += 1

    conn.commit()
    conn.close()
    print(f"✅ 完美！成功将 {count} 只经典与巅峰精灵注入到你的数据库中。")

if __name__ == "__main__":
    init_database()